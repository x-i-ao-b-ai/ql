window.left_nav = function(id){
	for (var i = 0; i < nav.length; i++) {
		if(nav[i].Tid == id){
			nav[i].active = true;
			$(".scl-nav").html("<h3><span>栏目列表</span></h3>"+$.templates('#left-nav_item').render(nav));
			$(".scm-nav").html('<a href="/">/<a href="'+nav[i].Name+'"></a>');
		}

		for (var j = 0; j < nav[i].Sub.length; j++) {
			if(nav[i].Sub != undefined){
				if(nav[i].Sub[j].Tid == id){
					nav[i].Sub[j].active = true;
					$(".scl-nav").html("<h3><span>栏目列表</span></h3>"+$.templates('#left-nav_item').render(nav[i].Sub));
					$(".scm-nav").html('<a href="/">首页</a>/<a href="#">'+nav[i].Name+'</a>/<a href="/'+nav[i].Sub[j].Ty+'/'+nav[i].Sub[j].Tid+'/List">'+nav[i].Sub[j].Name+'</a>');
				}
				
				if(nav[i].Sub[j].Sub != undefined){
					for (var g = 0; g < nav[i].Sub[j].Sub.length; g++) {
						if(nav[i].Sub[j].Sub[g].Tid == id){
							nav[i].Sub[j].Sub[g].active = true;
							$(".scl-nav").html("<h3><span>栏目列表</span></h3>"+$.templates('#left-nav_item').render(nav[i].Sub[j].Sub));

							if(nav[i].Sub[j].Link!=undefined){
								$(".scl-nav").append($.templates('#left-nav_item').render(nav[i].Sub[j].Link));
							}

							$(".scm-nav").html('<a href="/">首页</a>/<a href="#">'+nav[i].Name+'</a>/<a href="/'+nav[i].Sub[j].Ty+'/'+nav[i].Sub[j].Tid+'/List">'+nav[i].Sub[j].Name+'</a>');
						}
					};
				}
			}
		};
	};
};

Jxpage = function (str, page) {
    var urls = location.search;
    urls = urls.replace(/p[^&]*/, "");
    urls = urls.replace(/\?&/, "?");
    urls = urls.replace(/&&/, "&");
    if ((urls.match(/\?/) || []).length == 0) {
        urls += "?"
    }
    url = location.pathname + urls;

    var total = page[0]; //all number
    var page_curr = page[1];//当前的页面
    var page_num = page[2];//每个页面多少内容

    if (total == 0) {
        return
    }
    if (total % page_num == 0) {
        var page_all = (total - total % page_num) / page_num
    } else {
        var page_all = (total - total % page_num) / page_num + 1;//总页面数
    }
    //段长度
    var slice_num = 8;
    //起始页面
    var start = page_curr - page_curr % slice_num;
    //在一个段的第几个
    if (page_curr % slice_num != 0) {
        var curr = page_curr % slice_num;
    } else {
        var curr = 8;
        start = start - slice_num
    }

    //执行代码
    var len = slice_num;

    if (page_curr > (page_all - page_all % slice_num)) {
        len = page_all % slice_num;
    }
    var list_str = "";
    if (page_curr > 1) {
        list_str += "<li class='prev'><a href='" + url + "&p=" + (page_curr-1) + "'>上一页</a></li>";
    }
    for (var i = 1; i <= len; i++) {
        list_str = list_str + "<li ";
        if (i == curr) {
            list_str += "class='active' ><a href='javascript:void(0)'>" + (start + i) + "</a></li>";
        } else {
            list_str += "><a href='" + url + "&p=" + (start + i) + "'>" + (start + i) + "</a></li>";
        }

    }
    if (page_curr < page_all) {
        list_str = list_str + "<li class='next'><a href='" + url + "&p=" + (page_curr + 1) + "'>下一页</a></li>";
    }
    $("#" + str).html(list_str);
}